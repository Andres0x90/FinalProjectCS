    private void btn_filtrarListaActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_btn_filtrarListaActionPerformed
        tabla_clientePrincipal.setVisible(true);
        String resultLista = list_buscador.getSelectedItem().toString();

        switch (resultLista) {
            case "Mostrar clientes":
                String titulosClientes[] = {"cliente 1", "cliente 2", "cliente 3", "cliente 4"}; //titulo de las columnas
                String info = "1,2,3,4"; // Informaci√≥n que se va a enviar a la tabla
                String[] lineaClientes; // Array que almacenara la informaci√≥n separa por , en cada indice
                DefaultTableModel modeloCliente = new DefaultTableModel(); //Crear el modelo a la tabla

                modeloCliente.setColumnIdentifiers(titulosClientes); //Asignar titulos a la tabla
                lineaClientes = info.split(",");
                modeloCliente.addRow(lineaClientes); //Asignar el mododelo a la tabla
                tabla_ventas.setModel(modeloCliente);
                ;
                break;
            case "Mostrar ventas":
                String titulosVentas[] = {"c√≥digo", "cod_emp", "cod_cli", "cod_art", "Cant", "Valor", "Fecha", "Hora"}; //titulo de las columnas
                String infoVentas1 = "fila 1,002,001,003,10,150000,02-05-2020,8:30:36"; // Informaci√≥n que se va a enviar a la tabla
                String infoVentas2= "fila 2,003,004,005,2,3000,05-05-2010,8:20:36"; // Informaci√≥n que se va a enviar a la tabla
                String[] lineaVentas; // Array que almacenara la informaci√≥n separa por , en cada indice
                DefaultTableModel modeloVenta = new DefaultTableModel(); //Crear el modelo a la tabla

                modeloVenta.setColumnIdentifiers(titulosVentas); //Asignar titulos a la tabla
                String[] lineaVentas1 = infoVentas1.split(",");
                String[] lineaVentas2 = infoVentas2.split(",");
                modeloVenta.addRow(lineaVentas1); //Asignar el mododelo a la tabla
                modeloVenta.addRow(lineaVentas2);
                tabla_ventas.setModel(modeloVenta);
        }

        if (!(list_buscador.getSelectedItem() == "Mostrar ventas")) {
            
            //Quitar informaci√≥n si no estoy en la tabla ventas
            
            System.out.println("Ya no es listado de ventas");
            DefaultTableModel modeloCliente = new DefaultTableModel(); //Crear el modelo a la tabla
            tabla_clienteInterna.setModel(modeloCliente);
            modeloCliente.setRowCount(0);
            
            DefaultTableModel modeloEmpleado = new DefaultTableModel(); //Crear el modelo a la tabla
            tabla_empleadoInterna.setModel(modeloEmpleado);
            modeloEmpleado.setRowCount(0);
            
            DefaultTableModel modeloArticulo = new DefaultTableModel(); //Crear el modelo a la tabla
            tabla_articuloInterna.setModel(modeloArticulo);
            modeloArticulo.setRowCount(0);
        }

    }//GEN-LAST:event_btn_filtrarListaActionPerformed

